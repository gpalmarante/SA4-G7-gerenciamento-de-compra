package com.mycompany.sa4.Model;

public class Registros {
    private int id ;
    private String ultimaTela;
    private String nome;
    
    
    
	public String getUltimaTela() {
		return ultimaTela;
	}
	public void setUltimaTela(String ultimaTela) {
		this.ultimaTela = ultimaTela;
	}
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}
